<h1>Nueva bibliografía y Editar páginas:</h1>
La nueva bibliografía y la página de editar campos (Editar- Básica) son basados en en el formato standard <a href="javascript:popSecondaryLarge('http://www.loc.gov/marc')">MARC 21</a>.  Solo un subconjunto muy pequeño de cientos campos MARC son incluidos en esta página de Editar-Básica para hacer fácil agregar bibliografía. Una vez que la bibliografía se ha agregado, entonces, usted puede agregar cualquier campo de MARC 21 usando la página de "Editar-MARC".
<br><br>
Una vez que ha agregado una nueva bibliografía usted puede necesitar agregar ejemplares antes de que la bibliografía quede disponible para préstamos.
